export const getWordsByCategory = (category: Word['category']): Word[] => {
  return words.filter((word) => word.category === category);
};

export const getWordsByDifficulty = (difficulty: Word['difficulty']): Word[] => {
  return words.filter((word) => word.difficulty === difficulty);
};

export const getRandomWords = (count: number): Word[] => {
  const shuffled = [...words].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

export const searchWords = (query: string): Word[] => {
  const lowerQuery = query.toLowerCase();
  return words.filter(
    (word) =>
      word.word.toLowerCase().includes(lowerQuery) ||
      word.meaning.includes(query)
  );
};
